func TestIsTLSClientHello(t *testing.T) {
	tests := []struct {
		name     string
		data     []byte
		expected bool
	}{
		{"Valid ClientHello", sampleClientHello, true},
		{"Empty", []byte{}, false},
		{"Too short", []byte{0x16, 0x03}, false},
		{"Not TLS", []byte{0x17, 0x03, 0x01, 0x00, 0x05, 0x01}, false},
		{"Not ClientHello", []byte{0x16, 0x03, 0x01, 0x00, 0x05, 0x02}, false},
		{"HTTP request", []byte("GET / HTTP/1.1\r\n"), false},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			result := IsTLSClientHello(tt.data)
			if result != tt.expected {
				t.Errorf("IsTLSClientHello() = %v, want %v", result, tt.expected)
			}
		})
	}
}

func TestParseClientHello(t *testing.T) {
	info, err := ParseClientHello(sampleClientHello)
	if err != nil {
		t.Fatalf("ParseClientHello() error = %v", err)
	}

	if info.SNI != "www.google.com" {
		t.Errorf("SNI = %q, want %q", info.SNI, "www.google.com")
	}

	if info.SNIOffset <= 0 {
		t.Errorf("SNIOffset = %d, want > 0", info.SNIOffset)
	}

	if info.SNILength != 14 {
		t.Errorf("SNILength = %d, want 14", info.SNILength)
	}
}

func TestParseClientHello_Errors(t *testing.T) {
	tests := []struct {
		name string
		data []byte
		err  error
	}{
		{"Empty", []byte{}, ErrTruncated},
		{"Not TLS", []byte{0x17, 0x03, 0x01, 0x00, 0x05}, ErrNotTLS},
		{"Too short", []byte{0x16}, ErrTruncated},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			_, err := ParseClientHello(tt.data)
			if err == nil {
				t.Errorf("ParseClientHello() expected error, got nil")
			}
		})
	}
}
